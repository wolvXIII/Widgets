public class PlainBackground implements Background {

	private int color;

	/**
	 * Sets the color.
	 * 
	 * @param color
	 *            the color to set.
	 */
	public void setColor(int color) {
		this.color = color;
	}

	/**
	 * Gets the color.
	 * 
	 * @return the color.
	 */
	public int getColor() {
		return this.color;
	}

}
